let numbers = [1, 2, 3, 4, 5]

console.log(numbers)
//Add an element at the end of the array
numbers.push(1)
console.log(numbers)
//Add two more elements at the end of the array
numbers.push(3, 5)

console.log(numbers)

//2. Pop
//Remove an element from the end of the array

numbers.pop()
console.log(numbers)

//3. Shift
//Remove the first element in an array

numbers.shift()

console.log(numbers)


//4. Unshift
//Ad an eleement to the beginnning of an array
numbers.unshift(1)
console.log(numbers)